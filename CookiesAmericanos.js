//                                          Receita de Cokies

//  ingredientes:
// 2 tebletes de margarina/ manteiga ou 200g de manteiga
// 1 xícara de açúcar branco
// 1 xícara de açúcar mascavo
// 1 colher de sopa de essência de baunilha
// 2 ovos
// 3 xícaras de farinha
// 1 colher de sopa de fermento
// 1/2 colher de sopa de sal
// 2 xícaras de gotas de chocolate

//  preparo:
// coloque na batedeira a manteiga a temperatura ambiente, os açúcares e bater.
// acrescente os ovos e essência e bater. depois colocar sal, fermento, farinha e bater.
// por último acrecente as gotas de chocolate.
// faça bolinhas e coloque na assadeira e assar por no máximo 1o minutos.